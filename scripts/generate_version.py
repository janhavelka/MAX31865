#!/usr/bin/env python3
"""Generate include/MAX31865/Version.h from library.json."""

from __future__ import annotations

import json
import subprocess
from pathlib import Path


ENV = None
try:
    Import("env")  # type: ignore[name-defined]
    ENV = env  # type: ignore[name-defined]
except Exception:
    ENV = None


def _project_root() -> Path:
    if ENV is not None:
        return Path(ENV["PROJECT_DIR"]).resolve()
    return Path(__file__).resolve().parents[1]


ROOT = _project_root()
LIBRARY_JSON = ROOT / "library.json"
VERSION_H = ROOT / "include" / "MAX31865" / "Version.h"


def _git(args: list[str], default: str) -> str:
    try:
        return subprocess.check_output(
            ["git", *args],
            cwd=ROOT,
            stderr=subprocess.DEVNULL,
            text=True,
        ).strip() or default
    except Exception:
        return default


def _cpp_string_literal(value: str) -> str:
    escaped = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'\\"{escaped}\\"'


def _append_build_metadata_defines() -> None:
    if ENV is None:
        return
    commit = _git(["rev-parse", "--short", "HEAD"], "unknown")
    status = "dirty" if _git(["status", "--porcelain", "--untracked-files=no"], "") else "clean"
    ENV.Append(
        CPPDEFINES=[
            ("MAX31865_GIT_COMMIT", _cpp_string_literal(commit)),
            ("MAX31865_GIT_STATUS", _cpp_string_literal(status)),
        ]
    )


def _parse_semver(version: str) -> tuple[int, int, int]:
    core = version.split("-", 1)[0].split("+", 1)[0]
    parts = core.split(".")
    major = int(parts[0]) if len(parts) > 0 and parts[0].isdigit() else 0
    minor = int(parts[1]) if len(parts) > 1 and parts[1].isdigit() else 0
    patch = int(parts[2]) if len(parts) > 2 and parts[2].isdigit() else 0
    return major, minor, patch


def main() -> None:
    _append_build_metadata_defines()
    metadata = json.loads(LIBRARY_JSON.read_text(encoding="utf-8"))
    version = metadata.get("version", "0.0.0")
    major, minor, patch = _parse_semver(version)
    version_code = major * 10000 + minor * 100 + patch

    content = f'''/**
 * @file Version.h
 * @brief Version information for MAX31865.
 *
 * This file is AUTO-GENERATED by scripts/generate_version.py from library.json.
 * DO NOT EDIT MANUALLY. Update version metadata via library.json.
 */

#pragma once

#include <stdint.h>

#ifndef MAX31865_VERSION_STRING
#define MAX31865_VERSION_STRING "{version}"
#endif

#ifndef MAX31865_BUILD_DATE
#define MAX31865_BUILD_DATE __DATE__
#endif

#ifndef MAX31865_BUILD_TIME
#define MAX31865_BUILD_TIME __TIME__
#endif

#ifndef MAX31865_BUILD_TIMESTAMP
#define MAX31865_BUILD_TIMESTAMP MAX31865_BUILD_DATE " " MAX31865_BUILD_TIME
#endif

#ifndef MAX31865_GIT_COMMIT
#define MAX31865_GIT_COMMIT "unknown"
#endif

#ifndef MAX31865_GIT_STATUS
#define MAX31865_GIT_STATUS "unknown"
#endif

#ifndef MAX31865_VERSION_FULL
#define MAX31865_VERSION_FULL MAX31865_VERSION_STRING " (" MAX31865_GIT_COMMIT ", " MAX31865_BUILD_TIMESTAMP ", " MAX31865_GIT_STATUS ")"
#endif

namespace MAX31865 {{

static constexpr uint16_t VERSION_MAJOR = {major};
static constexpr uint16_t VERSION_MINOR = {minor};
static constexpr uint16_t VERSION_PATCH = {patch};
static constexpr const char* VERSION = MAX31865_VERSION_STRING;
static constexpr uint32_t VERSION_CODE = {version_code};
static constexpr int VERSION_INT = {version_code};
static constexpr const char* BUILD_DATE = MAX31865_BUILD_DATE;
static constexpr const char* BUILD_TIME = MAX31865_BUILD_TIME;
static constexpr const char* BUILD_TIMESTAMP = MAX31865_BUILD_TIMESTAMP;
static constexpr const char* GIT_COMMIT = MAX31865_GIT_COMMIT;
static constexpr const char* GIT_STATUS = MAX31865_GIT_STATUS;
static constexpr const char* VERSION_FULL = MAX31865_VERSION_FULL;

}}  // namespace MAX31865
'''
    VERSION_H.write_text(content, encoding="utf-8", newline="\n")


if __name__ == "__main__":
    main()
else:
    main()
